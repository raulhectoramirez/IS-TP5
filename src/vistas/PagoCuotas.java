    public void cargarCreditos(ArrayList<Cuota> todaslascuotas) {

       
        todaslascuotas.forEach((cuota)->{
            System.out.println(cuota.getFecha_vencimiento());
        });
        
                // CARGAMOS EL CREDITO EN LA VISTA
       
        String matriz[][] = new String[todaslascuotas.size()][5];
        String formato = "dd/MM/yyyy";
        SimpleDateFormat sdf = new SimpleDateFormat(formato);
        DecimalFormat formato_dec = new DecimalFormat("#.00");
        
   
        for(int i=0; i<todaslascuotas.size(); i++){
            
            matriz[i][0] = String.valueOf("  "+todaslascuotas.get(i).getNro());
            matriz[i][1] = String.valueOf(sdf.format(todaslascuotas.get(i).getFecha_vencimiento()));
            matriz[i][2] = String.valueOf("$ "+formato_dec.format(todaslascuotas.get(i).getMonto()));
            matriz[i][3] = String.valueOf("$ "+formato_dec.format(todaslascuotas.get(i).getRecargo()));
            matriz[i][4] = String.valueOf(todaslascuotas.get(i).getEstado());
            
            
        }
        tablaCuotas.setModel(new javax.swing.table.DefaultTableModel(matriz, new String[]{
            "NÂº Cuota","Vencimiento", "Importe","Recargo","Estado"
        }));
        
        
        
        
    }


